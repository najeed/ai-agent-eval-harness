import json
from pathlib import Path
from typing import List, Dict, Any
from .trace_utils import load_events

class LeaderboardGenerator:
    """Aggregates results from multiple evaluation runs into a comparison leaderboard."""

    @staticmethod
    def generate_markdown(runs_dir: str) -> str:
        """Generates a Markdown leaderboard table from traces in a directory."""
        path = Path(runs_dir)
        traces = list(path.glob("*.jsonl"))
        
        stats = []
        for tp in traces:
            try:
                events = load_events(tp)
                run_start = next((e for e in events if e.get("event") == "run_start"), {})
                meta = run_start.get("metadata", {})
                
                # 1. Improved Attribution
                agent_name = meta.get("agent_name") or meta.get("agent")
                if not agent_name:
                    # Fallback: Clean up filename (e.g., run-accuracy-test-123 -> Accuracy-Test)
                    clean_name = tp.stem.replace("run-", "").replace("-test", "")
                    # Convert kebab-case to Title Case
                    agent_name = " ".join([w.capitalize() for w in clean_name.split("-") if not w.isdigit()])
                    if not agent_name:
                        agent_name = tp.stem
                
                # Simple pass rate calculation
                evals = [e for e in events if e.get("event") == "evaluation"]
                if not evals:
                    continue
                    
                total_tasks = len(set(e.get("task_id") for e in evals))
                successful_tasks = 0
                
                task_metrics = {}
                for e in evals:
                    tid = e.get("task_id")
                    if tid not in task_metrics:
                        task_metrics[tid] = []
                    task_metrics[tid].append(e.get("success", False))
                
                for tid, results in task_metrics.items():
                    if all(results):
                        successful_tasks += 1
                
                pass_rate = (successful_tasks / total_tasks * 100) if total_tasks > 0 else 0
                
                # 2. Quality Threshold (50% Pass Rate)
                if pass_rate < 50:
                    continue

                stats.append({
                    "agent": agent_name,
                    "pass_rate": pass_rate,
                    "tasks": f"{successful_tasks}/{total_tasks}",
                    "file": tp.name
                })
            except Exception:
                continue

        if not stats:
            return "# 🏆 Agent Evaluation Leaderboard\n\n> [!WARNING]\n> No agents have achieved the minimum quality threshold (50% pass rate) yet. Keep iterating!\n"

        # Sort by pass rate descending
        stats.sort(key=lambda x: x["pass_rate"], reverse=True)

        md = "# 🏆 Agent Evaluation Leaderboard\n\n"
        md += "| Rank | Agent | Pass Rate | Success/Total | Trace File |\n"
        md += "| :--- | :--- | :--- | :--- | :--- |\n"
        
        for i, s in enumerate(stats):
            icon = "🥇" if i == 0 else "🥈" if i == 1 else "🥉" if i == 2 else f"{i+1}."
            md += f"| {icon} | **{s['agent']}** | {s['pass_rate']:.1f}% | {s['tasks']} | `{s['file']}` |\n"
            
        md += "\n*Generated by [MultiAgentEval](https://github.com/najeed/ai-multi-agent-eval)*\n"
        return md

def run_leaderboard(runs_dir: str, output_file: str = "LEADERBOARD.md"):
    """CLI helper for generating the leaderboard."""
    print(f"\n[Leaderboard] Aggregating results from: {runs_dir}...")
    content = LeaderboardGenerator.generate_markdown(runs_dir)
    with open(output_file, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"[OK] Leaderboard generated: {output_file}")
